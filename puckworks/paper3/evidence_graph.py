DATASET_STATUS = ("resolved_manifest", "not_applicable_code", "not_applicable_source_equation",
                  "not_applicable_synthetic", "proposed_experiment", "blocked_missing",
                  "needs_adjudication")
EVIDENCE_ROLES = ("fit", "eval", "reference", "context")
