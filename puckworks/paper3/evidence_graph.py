def write(root=REPO_ROOT):
    out_dir = Path(root) / GENERATED_REL
    out_dir.mkdir(parents=True, exist_ok=True)
    for rel, content in generate().items():
        (out_dir / rel).write_text(content, encoding="utf-8")
    return sorted(generate())


def verify(root=REPO_ROOT):
    out_dir = Path(root) / GENERATED_REL
    stale = []
    for rel, content in generate().items():
        p = out_dir / rel
        if (not p.exists()) or p.read_text(encoding="utf-8") != content:
            stale.append(rel)
    return stale


def main(argv=None):
    import sys
    argv = sys.argv[1:] if argv is None else argv
    if "--reconcile" in argv:
        strict = "--strict" in argv
        scope = "all"
        if "--scope" in argv:
            i = argv.index("--scope")
            scope = argv[i + 1] if i + 1 < len(argv) else "all"
        problems = reconcile(strict=strict, scope=scope)
        tag = (" --strict --scope %s" % scope) if strict else ""
        if problems:
            print("EVIDENCE GRAPH RECONCILE FAILED%s (%d):" % (tag, len(problems)))
            for p in problems:
                print("  -", p)
            return 1
        print("evidence graph reconciles with the live registry%s" % tag)
        return 0
    if "--bootstrap" in argv:
        print(json.dumps(bootstrap_missing(), indent=2))
        return 0
    if "--verify" in argv:
        s = verify()
        print("stale artifacts:", s or "none")
        return 1 if s else 0
    if "--write" in argv:
        print("wrote:", write())
        return 0
    print(json.dumps({k: len(v) for k, v in generate().items()}, indent=2))
    return 0


